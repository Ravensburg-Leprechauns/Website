Baseball ist in der US-Sportlandschaft eine Institution. Neben American Football,
 Basketball und Eishockey gehört es zu den sogenannten „Big Four“, den vier größten und populärsten Sportarten in den USA.
 Aber auch in der Karibik (z.B. Cuba, Puerto Rico,…) und Ostasien (Japan, Südkorea,…) gehört Baseball zu den Nationalsportarten.
 <br>
  <br>
Die Gründungsgeschichte des ravensburger Baseballclubs liest sich allerdings nicht amerikanisch, spanisch oder japanisch… 
Vielmehr ging es bei den Anfängen der Ravensburg Leprechauns, so der Name der Baseballmannschaft des TSB Ravensburg, irisch zu.
 <br>
  <br>
Mirko Wengrzik und Stefan Janezic sind Strippenzieher der ersten Stunde. 
Mit einigen weiteren Baseball-Begeisterten und -Interessierten saßen sie im März des Jahres 2012 (wie so oft) im Ravensburger Irish Pub zusammen.
 „Wir haben damals gesagt: ‚Lasst uns mal wieder Baseball spielen, ein paar Bälle werfen‘“.
 Und tatsächlich: Wenig später standen fortan jeden Sonntag Spieler auf dem alten, frei zugänglichen Trainingsplatz der FH Weingarten.
 Gemeinsam mit seinem Trainerkollegen Rainer Seibold formte Wengrzik eine Baseballtruppe. 4, 6, 10, 12 Spieler und Spielerinnen. 
 Der Bekanntheitsgrad der Trainingsgruppe zog immer größere Kreise. Die Belohnung: 
 2013 nahm der TSB Ravensburg die Baseballer als neue Abteilung auf. Bei der Namensgebung ging es dann wieder irisch zu: 
 Die Ravensburg Leprechauns sind nach einem irischen Kobold benannt. Durch die Gründung im Irish Pub lag das auf der Hand. 
  <br>
Ebenfalls logisch war der nächste Schritt, den die Leprechauns wagten. Im Jahr 2014 nahmen sie erstmals am Ligabetrieb teil.
 Waren es 2013 nur Testspiele, so traten die Ravensburger nun in der Bezirksliga an. Hohe Ansprüche stellte die Mannschaft,
 die inzwischen auch Zulauf von alten Hasen mit 1. Liga Erfahrung und absoluten Beginnern bekommen hatte aber nicht.
 „Wir wollen Erfahrung sammeln und gucken, wie das Ganze abläuft“, kündigt der Trainer an.
 Eine erste Kostprobe gab es schon im baden-württembergischen Pokal. Gegen Verbandsligist Nagold hielt Ravensburg lange gut mit, 
 musste letztlich aber eine deutliche 9:19-Niederlage einstecken. Es war das erste offizielle Pflichtspiel in der Geschichte des Vereins.
 <br>
  <br>
Die folgenden zwei Spielzeiten waren nicht nur was den Erfahrungszuwachs angeht ein Erfolg. 
Beide Male schlossen die „Leps“ die Saison auf dem ordentlichen dritten Platz ab. 
In der Saison 2015 konnte man sogar eine Pokalüberraschung erlangen in der man den Zweitligist Karlsruhe zu Hause
 schlug und erst gegen die Heidenheim Heideköpfe ausschied, die sich nur wenige Wochen später die deutsche Meisterschaft sicherten!
  <br>
   <br>
2016 folgte dann ein weiterer wichtiger und folgerichtiger Schritt in der Historie des ravensburger Baseballs.
 Am 29.04. startete die Jugendmannschaft ihren Trainingsbetrieb.
  <br>
   <br>
Auch abseits des Spielbetriebs warten in Zukunft weitere Aufgaben auf den jungen Verein.
 Spieler, Sponsoren und Zuschauer müssen geködert, Schiedsrichter ausgebildet, die Mannschaften etabliert werden. 
 Gerade bei einer Randsportart wie Baseball ist das keine Selbstverständlichkeit und nur mit viel Engagement zu meistern. 
  <br>
   <br>
Auch wenn es bei den Leprechauns sehr irisch zuging: Die „Big Four“ des US-Sports sind auch in Ravensburg wieder komplettiert. 
