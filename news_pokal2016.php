	<h2> Pokalaus für die Ravensburg Leprechauns
	 </h2>
	<hr>
	<br>
	
	<img id="news_bild" src="Galerie/BilderNewsLeprechauns/Rav_Gam_kl.jpg" />
Die Baseballer der Ravensburg Leprechauns sind am vergangenen Sonntag gegen die höherklassigen Ellwangen Elks in der ersten Pokalrunde des Baden-Württembergischen Baseball-Pokals ausgeschieden.
<br>
<br>
Bei trübem Wetter hat das Spiel auf dem Kunstrasenplatz des TSB Ravensburg spannend begonnen. 
Beide Mannschaften konnten mit einer starken Offensive frühe Akzente setzen. 
Aufgrund immer stärkeren Regens musste das Spiel vom Schiedsrichtergespann vorübergehend unterbrochen werden, 
als die Ravensburger im 3. von geplanten 9 Spielabschnitten  4:3 zurück lagen. 
Nach der Regenpause kamen die Ellwanger Landesligisten dann deutlich besser in Schwung und konnten schnell ihren Vorsprung ausbauen und bis zum 5. Spielabschnitt auf 9:4 erhöhen. 
<br>
<br>
Durch das noch immer kalte und verregnete Wetter haben die Schiedsrichter dann das Spiel aus Sicherheitsgründen 
(nasse, harte Bälle bei kalten Händen und zunehmend wenig Kontrolle beim Werfen können zu Verletzungen führen) endgültig abgebrochen, 
wodurch leider keine Aufholjagd der Leprechauns mehr möglich war. Das Spiel wurde somit mit 9:4 gewertet,
 wodurch die Ellwanger in die nächste Pokalrunde einziehen.
 <br>
 <br>
Die Ravensburg Leprechauns hingegen können sich nun gänzlich auf die kommende Saison in der Bezirksliga und den Start der neuen Jugendmannschaft konzentrieren, 
möchten sich aber auch von ganzem Herzen bei allen Fans bedanken, 
die dem Aprilwetter trotzten und am Platz ihre Ravensburger Baseballer unterstützt haben. Mit dieser Unterstützung, 
der geschlossenen Mannschaftsleistung und ein wenig mehr Wohlwollen des Wetters ist in der Saison 2016 noch Vieles möglich. 
Zeigen können dies die „Leps“ bereits am 8. Mai beim ersten Saisonspiel in Aichelberg.
