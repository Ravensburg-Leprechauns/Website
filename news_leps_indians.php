	<h2> Die Ravensburg Leprechauns schlagen den Tabellenführer 9:5
	 </h2>
	<hr>
	<br>
	
	<img id="news_bild" src="Galerie/BilderNewsLeprechauns/news_leps_indians_kl.png" />
Die Abteilung Baseball des TSB Ravensburg schlägt den bis dahin ungeschlagenen Tabellenführer der Bezirksliga Gruppe 1 aus Aichelberg in einem spannenden Spiel 9:5.
<br>
<br>
Bei größtenteils perfektem Baseballwetter waren auf dem Kunstrasenplatz des TSB Ravensburg am vergangenen Samstag die Aichelberg Indians zu Gast und wollten ihren fünften Sieg in Folge einfahren. 
Die Ravensburg Leprechauns ihrerseits wollten ihr Heimpublikum für die Niederlage vor 2 Wochen entschädigen und dem Tabellenführer die erste Niederlage zufügen. 
Bei ausgezeichneter Stimmung um und auf dem Platz stand einem großartigen Tag Baseball also nichts mehr im Wege.
<br>
<br>
Die Leps erwischten dann auch eindeutig den besseren Start ins Spiel und konnten in den ersten 5 Innings einen 7:0 Vorsprung herausspielen. 
Dies lag zum Einen an einer sehr guten Defensivarbeit, zum Anderen aber auch an einer cleveren und disziplinierten Offensive, die den Gegner konstant unter Druck setzte und Inning für Inning den Vorsprung ausbauen konnte. 
Den größten Anteil an der deutlichen Führung zu Beginn des sechsten Innings hatte aber vermutlich Starting Pitcher Marc Ullrich, der bis zu diesem Zeitpunkt noch keinen einzigen erfolgreichen Schlag der Indians zugelassen hatte. 
Erst im sechsten Inning konnten die Gäste aus Aichelberg einen Punkt erzielen.
 Die Dominanz des Ravensburger Pitchers hat sich am Ende mit 14 Strike-Outs bei nur 2 Base-on-Balls und einem zugelassenen Punkt nach 7 geworfenen Innings deutlich gezeigt.
<br>
<br>
Im achten Inning übernahm dann Einwechselwerfer Adrian Gaerste und sah sich mit einer erstarkenden Aichelberger Offensive konfrontiert.
 Der Tabellenführer wollte sich nicht so einfach mit der ersten Niederlage abfinden und konnte durch weitere 4 Punkte das Spiel nochmal spannend gestalten, 
 ehe das Spiel im neunten Inning mit zwei Läufern auf Base durch 2 Strike-Outs beendet werden konnte.
<br>
<br>
Die Aichelberg Indians bleiben trotz ihrer ersten Niederlage weiterhin Tabellenführer, während die Ravensburg Leprechauns nun mit je 2 Siegen und Niederlagen den Sprung auf Tabellenplatz 2 im Visier haben.
