	<h2> Erstes Jugendtraining im Ravensburger Baseball
	 </h2>
	<hr>
	<br>
	
	<img id="news_bild" src="Galerie/BilderNewsLeprechauns/training_jm_kl.JPG" />
Am vergangenen Freitag hat in Ravensburg auf dem Kunstrasenplatz des TSB das erste Jugendtraining der Abteilung Baseball stattgefunden. 
<br>
<br>
Rechtzeitig zum ersten Jugendtraining in der Geschichte der Ravensburg Leprechauns hat der Frühling den Winter vertrieben. Bei strahlendem Sonnenschein haben sich 15 hoch motivierte Jugendliche zwischen 8 und 18 zusammen gefunden,
um in die doch eher unbekannte Sportart Baseball hinein zu schnuppern.
<br>
<br>
Nach einem kurzen und intensiven Warm-Up wurden dann auch schon die ersten Bälle geworfen.
Nach den ersten Übungswürfen wurden die Ziele immer besser getroffen und es hat sich bei einigen Jugendlichen vielversprechendes Talent gezeigt.
Dieser Eindruck wurde beim folgenden Schlagtraining bestätigt, als alle Jugendlichen zum ersten Mal einen Baseball in das Fangnetz schlagen durften.
<br>
<br>
Damit es nicht bei Grundlagen bleibt und der Spaß nicht zu kurz kommt, wurde nachfolgend ein kurzes Trainingsspiel absolviert. 
Hierbei konnten alle Anwesenden die gelernten Lektionen sofort umsetzen und ein schönes, schnelles Spiel zustande bringen.
<br>
<br> 
Bereits nächsten Freitag von 16:30 bis 18:00 geht es wieder los, wenn das Trainerteam um den international erfahrenen Cheftrainer Sonel Güresci den „Little-Leps“ weitere Grundlagen beibringen und gemeinsam viel Spaß haben wollen.
Die Baseballer des TSB Ravensburg würden sich natürlich auch freuen, weitere neue Gesichter im neuen Jugendtraining begrüßen zu dürfen.

