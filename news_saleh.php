	<h2> Die Ravevnsburg Leprechaus starten mit neuem Hauptsponsor in die Saison 2016
	 </h2>
	<hr>
	<br>
	
	<img id="news_bild" src="Galerie/BilderNewsLeprechauns/saleh_kl.png" />
	Die Ravensburg Leprechaus sind sehr erfreut einen neuen Hauptsponsor für die Saison 2016 gefunden zu haben. Mit Hussein Saleh haben wir einen sehr motivierten Partner gefunden,
	der laut eigener Aussage, sehr gerne kleinere Vereine unterstützt.
	<br>
	<br>
	Nach einer Ausbildung zum Friseur und einer Weiterbildung zum Friseurmeister mit anschließender langjähriger Erfahrung bei Star Coiffeur Lipperts in München, konnte Hussein Saleh bereits im Alter von 24 Jahren seinen Traum einen eigenen Friseursalon zu eröffnen, verwirklichen. Hussein Saleh überzeugte vor allem aufgrund seiner Begeisterung für diesen Beruf, seinem Ehrgeiz, seiner grenzenlosen Kreativität, seiner Zielstrebigkeit, sowie seiner Professionalität, wodurch er eine Vielzahl an Kunden gewinnen konnte.

	Neben seinen außerordentlichen Fähigkeiten, die er als Friseurmeister beherrscht, bietet er auch aufgrund seiner Herkunft aus dem Libanon arabische Techniken wie Haarentfernung mit Bindfaden oder Flamme an. Hussein Saleh möchte immer mehr erreichen und auf dem aktuellsten Stand der neuesten Trends sein. Hierfür ist ihm kein Weg zu weit. Auch an Schulungen in

	Los Angeles nahmen Hussein Saleh und sein Team teil.

	Des Weiteren konnte Hussein Saleh nicht nur durch erfolgreich gewonnene Wettbewerbe, sondern auch bei der Berliner Fashion Week mit seinem Handwerk überzeugen. Aufgrund seines Erfolgs beschäftigt Hussein Saleh mittlerweile mehrere Mitarbeiter in seinem Friseursalon in Ravensburg.
	<br>
	<br>
