	<h2> Die Ravensburg Leprechauns gründen eine Jugendmannschaft
	 </h2>
	<hr>
	<br>
	
	<img id="news_bild" src="Galerie/BilderNewsLeprechauns/jm_kl.png" />
Die Ravensburg Leprechauns können dank erweiterten Platzzeiten durch den TSB Ravensburg ab 29. April 2016 mit dem Trainingsbetrieb einer Jugendmannschaft beginnen. 
<br>
Trainingsbeginn ist ab dem 29. April 2016 immer Freitags um 16:30 Uhr am Kunstrasenplatz des TSB Ravensburg. 
Die Mädchen und Jungs von 8 bis 18 Jahren sind dann um 18:00 Uhr mit dem Training fertig.
<br>
<br>
Da gerade im Baseball taktisches Verständnis, Umgang mit Rückschlägen und eine große Menge Teamwork gefragt sind, 
sind die Baseballer des TSB Ravensburg besonders glücklich, 
 mit Sonel Güresci einen Chef-Trainer mit reichhaltiger nationaler und internationaler Baseball-Erfahrung gesichert zu haben. 
 Doch auch das restliche Trainerteam um Coach Güresci kann auf viele Jahre Erfahrung in den obersten Ligen Deutschlands zurück blicken.
 <br>
 <br>
Dank diesem geballten Know-How sind die Leprechauns äusserst zuversichtlich, 
eine schlagkräftige Jugendgruppe zusammenstellen zu können und unserer Jugend alle Tugenden mit auf den Weg zu geben, um nicht nur im Baseball, 
sondern auch abseits des Platzes zu wachsen.
<br>
<br>
Sowohl unser Trainerteam als auch unsere Jugendspieler sind dennoch auf weitere Unterstützung angewiesen, 
denn immerhin stehen durch den Neuaufbau der Jugendmannschaft diverse Investitionen an (Jugendschläger, -handschuhe, -helme, etc.). 
Aus diesem Grund würden sich die Ravensburger Baseballer über jegliche Unterstützung seitens neuer Sponsoren oder Spenden freuen.
<br>
<br>
Aber nicht nur Sponsoren und Förderer werden gesucht, sondern auch alle interessierten Mädchen und Jungs zwischen 8 und 18 Jahren, 
die einen etwas anderen Sport ausprobieren wollen. Aber auch alle Anderen, die evtl.
 bei Heimspielen bei der Bewirtung oder Organisation unterstützen wollen, sind herzlich eingeladen,
 mit dem Trainerteam in Verbindung zu setzen oder bei einem der Trainings persönlich vorbei zu schauen.
 <br>
 <br>
Mit diesen Aspekten vor Augen freuen sich schon alle Spielerinnen,
 Spieler und Trainer der Ravensburg Leprechauns auf die Saison 2016 und die neue Herausforderung „Jugendmannschaft“ und hoffen, 
 bei den ersten Trainings und Spielen viele neue Gesichter zu sehen.
	<br>
