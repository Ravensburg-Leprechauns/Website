	<h2> Die Ravensburg Leprechauns feiern 16:6 Erfolg im ersten Heimspiel
	 </h2>
	<hr>
	<br>
	
	<img id="news_bild" src="Galerie/BilderNewsLeprechauns/news_22-05-2016_kl.jpg" />
Die Ravensburg Leprechauns konnten am vergangenen Sonntag bei perfektem Baseball- Wetter einen ungefährdeten 16:6 Erfolg gegen die Ulm Falcons feiern.
<br>
<br>
Der Heimauftakt der Ravensburg Leprechauns mit ihrem neuen Hauptsponsor Hussein Saleh hätte kaum besser beginnen können. 
Die angereisten Zuschauer konnten bereits im ersten Spielabschnitt mitverfolgen, wie die Baseballer des TSB Ravensburg mit 7 Punkten in Führung gingen. 
Dabei konnten die Leps nicht nur vom anfangs unsicheren Pitching der Ulmer Wurfmänner profitieren, sondern überzeugten durch sehr cleveres und diszipliniertes Verhalten am Schlag. 
Die beiden folgenden Spielabschnitte konnten die Falcons dann bis auf 3 Punkte aufholen, ehe die Ravensburger in der Offensive wieder auftrumpfen und auf 12:3 erhöhen konnten.
<br>
<br>
Den letzten Widerstand der Ulmer Baseballer konnten die Leps durch solide Defensive beenden und ihrerseits durch starke Offensive kontern. 
Somit konnten die Schiedsrichter das Spiel nach dem siebten von neun Spielabschnitten aufgrund der 10-Punkte-Regel abbrechen und den 16:6 Erfolg für die Leprechauns werten.
<br>
<br>
Das Spiel war aber nicht nur aufgrund des Ergebnisses ein voller Erfolg, sondern auch durch den Einsatz einiger neuer Spieler, die sich sofort gut ins Team einfügen und einen wertvollen Beitrag leisten konnten. 
Nun haben die Leps Zeit, um sich auf das nächste Heimspiel am Samstag den 4. Juni um 15:00 auf dem Kunstrasenplatz des TSB Ravensburg vorzubereiten, 
wenn die Gammertingen Royals zu Gast sind und es um Platz 2 in der Bezirksliga Gruppe 1 geht.
